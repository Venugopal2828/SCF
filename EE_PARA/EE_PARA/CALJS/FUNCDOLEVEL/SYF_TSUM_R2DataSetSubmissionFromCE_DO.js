            var sum_TSU_TTL_NET_AMT = SYS_getFieldSumValue(pNode, 'TSU_TTL_NET_AMT');

            var ppNode = SYS_getParentDo(pNode);
            ppRecord = SYS_getParentRecord(pNode);
