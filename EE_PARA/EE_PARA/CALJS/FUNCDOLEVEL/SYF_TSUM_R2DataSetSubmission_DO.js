function R2ComrclDataset_R2DT_ComrclDocRef_R2DT_Goods_R2DT_ComrclLineItms(node, recordId, status) {
    try {
        pRecord = SYS_getParentRecord(node);
        var TSU_COMM_REF = pRecord.TSU_COMM_REF;
        var TSU_PO_ID = pRecord.TSU_PO_ID;
        if (status == 'E') {
            // Get sun of the Lineitems' brothers sum values
            var nAdjust_node = SYS_getBrotherDo(node, 'DT_Adjstmnt');
            var sum_Adjust = SYS_getFieldSumValue(nAdjust_node, 'TSU_IAFT_AMT_CAL');
            var nFreight_node = SYS_getBrotherDo(node, 'DT_FrghtChrgs');
            var sum_Freight = SYS_getFieldSumValue(nFreight_node, 'TSU_IAFT_AMT');
            var nTax_node = SYS_getBrotherDo(node, 'DT_Tax');
            var sum_Tax = SYS_getFieldSumValue(nTax_node, 'TSU_IAFT_AMT');

            //Get LineItems' children sum values
            var nodes = SYS_getChildNodes(node, recordId);
            var len = nodes.length;
            for (var i = 0; i < len; i++) {
                var _do = nodes[i];
                var doName = SYS_getDoName(_do);

                if (doName == "DT_Adjstmnt") {
                    var nTSU_IAFT_AMT_CAL = SYS_getFieldSumValue(_do, 'TSU_IAFT_AMT_CAL');
                    nAdjstmnt = SYS_BeFloat(nTSU_IAFT_AMT_CAL);
                } else if (doName == "DT_FrghtChrgs") {
                    var nTSU_IAFT_AMT = SYS_getFieldSumValue(_do, 'TSU_IAFT_AMT');
                    nFrghtChrgs = SYS_BeFloat(nTSU_IAFT_AMT);
                } else if (doName == "DT_Tax") {
                    var nTSU_IAFT_AMT = SYS_getFieldSumValue(_do, 'TSU_IAFT_AMT');
                    nTax = SYS_BeFloat(nTSU_IAFT_AMT);
                }
            }


            currRecord = SYS_getRecords(node);
            var TSU_QTY_VAL = currRecord[recordId].TSU_QTY_VAL;
            var TSU_UNITPRIC_AMT = currRecord[recordId].TSU_UNITPRIC_AMT;
            var AMT = SYS_BeFloat(TSU_QTY_VAL) * SYS_BeFloat(TSU_UNITPRIC_AMT);
            var sum_curr_ComrclLineItms = SYS_BeFloat(AMT) + SYS_BeFloat(nAdjstmnt) + SYS_BeFloat(nFrghtChrgs) + SYS_BeFloat(nTax);
            SYS_setFieldValue(node, recordId, "TSU_TTL_AMT", sum_curr_ComrclLineItms);

            //Get sum of ComrclLineItms
            var sum_ComrclLineItms = SYS_getFieldSumValue(node, 'TSU_TTL_AMT');

            //Get sum of the total
            var sum = SYS_BeFloat(sum_ComrclLineItms) + SYS_BeFloat(sum_Adjust) + SYS_BeFloat(sum_Freight) + SYS_BeFloat(sum_Tax);
            //Set values to Goods
            var pNode = SYS_getParentDo(node);
            pRecord = SYS_getParentRecord(node);
            recorId = pRecord.recordID;
            SYS_setFieldValue(pNode, recorId, "TSU_LINE_TTL_AMT", sum_ComrclLineItms);
            SYS_setFieldValue(pNode, recorId, "TSU_TTL_NET_AMT", sum);

            var sum_TSU_TTL_NET_AMT = SYS_getFieldSumValue(pNode, 'TSU_TTL_NET_AMT');

            var ppNode = SYS_getParentDo(pNode);
            ppRecord = SYS_getParentRecord(pNode);
            precorId = ppRecord.recordID;
            SYS_setFieldValue(ppNode, precorId, "TSU_COMM_AMT", sum_TSU_TTL_NET_AMT);
            SYS_RefreshGrid();
        }

        if (status == 'A' || status == 'E') {
            SYS_setFieldValue(node, recordId, "TSU_COMM_REF", TSU_COMM_REF);
            SYS_setFieldValue(node, recordId, "TSU_PO_ID", TSU_PO_ID);
            //Get LineItems' brother Sum Value
            var nAdjust_node = SYS_getBrotherDo(node, 'DT_Adjstmnt');
            var sum_Adjust = SYS_getFieldSumValue(nAdjust_node, 'TSU_IAFT_AMT_CAL');
            var nFreight_node = SYS_getBrotherDo(node, 'DT_FrghtChrgs');
            var sum_Freight = SYS_getFieldSumValue(nFreight_node, 'TSU_IAFT_AMT');
            var nTax_node = SYS_getBrotherDo(node, 'DT_Tax');
            var sum_Tax = SYS_getFieldSumValue(nTax_node, 'TSU_IAFT_AMT');
            //Get sum of ComrclLineItms
            var sum_ComrclLineItms = SYS_getFieldSumValue(node, 'TSU_TTL_AMT');
            sum_ComrclLineItms = SYS_BeFloat(sum_ComrclLineItms);
            var sum = SYS_BeFloat(sum_ComrclLineItms) + SYS_BeFloat(sum_Tax) + SYS_BeFloat(sum_Adjust) + SYS_BeFloat(sum_Freight);

            //Set LineItems Total Amount and LineItems Total Net Amount to Goods


            var pNode = SYS_getParentDo(node);
            pRecord = SYS_getParentRecord(node);
            recorId = pRecord.recordID;
            SYS_setFieldValue(pNode, recorId, "TSU_LINE_TTL_AMT", sum_ComrclLineItms);
            SYS_setFieldValue(pNode, recorId, "TSU_TTL_NET_AMT", sum);

            var sum_TSU_TTL_NET_AMT = SYS_getFieldSumValue(pNode, 'TSU_TTL_NET_AMT');

            var ppNode = SYS_getParentDo(pNode);
            ppRecord = SYS_getParentRecord(pNode);
            precorId = ppRecord.recordID;
            SYS_setFieldValue(ppNode, precorId, "TSU_COMM_AMT", sum_TSU_TTL_NET_AMT);
            SYS_RefreshGrid();
        }
    } catch (e) {
        DisExcpt("SYF_TSUM_R2DataSetSubmission_DO.js", e);
    }
}

