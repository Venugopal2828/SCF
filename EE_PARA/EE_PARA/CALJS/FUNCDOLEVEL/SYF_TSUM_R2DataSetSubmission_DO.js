function R2ComrclDataset_R2DT_ComrclDocRef_R2DT_Goods(node, recordId, status) {
    try {
        if (status == 'A') {
            pRecord = SYS_getParentRecord(node);
            var TSU_COMM_REF = pRecord.TSU_COMM_REF;
            SYS_setFieldValue(node, recordId, "TSU_COMM_REF", TSU_COMM_REF);
            var mainccy = SYS_getCurrDoScreenValue("TSU_CCY");
            SYS_setValueToMain("TSU_CCY", mainccy);

            var sum_TSU_TTL_NET_AMT = SYS_getFieldSumValue(node, 'TSU_TTL_NET_AMT');

            var pNode = SYS_getParentDo(node);
            pRecord = SYS_getParentRecord(node);
            precorId = pRecord.recordID;
            SYS_setFieldValue(pNode, precorId, "TSU_COMM_AMT", sum_TSU_TTL_NET_AMT);
            SYS_RefreshGrid();
        } else if (status == 'E') {
            pRecord = SYS_getParentRecord(node);
            var TSU_COMM_REF = pRecord.TSU_COMM_REF;
            SYS_setFieldValue(node, recordId, "TSU_COMM_REF", TSU_COMM_REF);
            var mainccy = SYS_getCurrDoScreenValue("TSU_CCY");
            SYS_setValueToMain("TSU_CCY", mainccy);

            var sum_TSU_TTL_NET_AMT = SYS_getFieldSumValue(node, 'TSU_TTL_NET_AMT');

            var pNode = SYS_getParentDo(node);
            pRecord = SYS_getParentRecord(node);
            precorId = pRecord.recordID;
            SYS_setFieldValue(pNode, precorId, "TSU_COMM_AMT", sum_TSU_TTL_NET_AMT);

            var RltdTxRefs_node = SYS_getNodeByXpath('R2RltdTxRefs');
            var TSU_FORCD_MTCH = SYS_getCurrDoScreenValue("TSU_FORCD_MTCH_FLG");
            var TSU_PO_ID = SYS_getCurrDoScreenValue("TSU_PO_ID");
            var TSU_SUBMITR_TRX_REF = SYS_getCurrDoScreenValue("TSU_SUBMITR_TRX_REF");
            var TSU_TID = SYS_getCurrDoScreenValue("TSU_TID");

            Pnode = SYS_getNodeByXpath('R2RltdTxRefs');
            var records = SYS_getRecords(Pnode);
            var len = records.length;
            for (var i = 0; i < len; i++) {
                record = records[i];
                id = record.recordID;
                if (record.TSU_PO_ID == TSU_PO_ID) {
                    SYS_setFieldValue(Pnode, id, "TSU_FORCD_MTCH_FLG", TSU_FORCD_MTCH);
                    SYS_setFieldValue(Pnode, id, "TSU_SUBMITR_TRX_REF", TSU_SUBMITR_TRX_REF);
                    SYS_setFieldValue(Pnode, id, "TSU_TID", TSU_TID);
                }
            }
            SYS_RefreshGrid();
        } else if (status == 'D') {
            var deletePath1 = "R2ComrclDataset.DT_PmtTerms";
            var deletePath2 = "R2ComrclDataset.R2DT_SttlmTerms";
            var deletePath3 = "R2RltdTxRefs";
            var records = SYS_GetDeleteRecords(node);
            for (var i = 0; i < records.length; i++) {
                var record = records[i];
                var id = record['TSU_PO_ID'];
                var namevalueObj = {
                    TSU_PO_ID: id
                };
                var Paymentnode = SYS_getNodeByXpath('deletePath1');
                var Paymentrecords = SYS_getRecords(Paymentnode);
                for (var k = 0; k < Paymentrecords.length; k++) {
                    Payrecord = Paymentrecords[k];
                    var TSU_PO_ID = Payrecord.TSU_PO_ID;
                    if (TSU_PO_ID == namevalueObj) {
                        SYS_FilterRecordByXpath(deletePath1, namevalueObj);
                    }
                }
                // Modified by Mia--To check whether settlement record is available on the current PO Ref. If available, delete it. --Start 
                var Settnode = SYS_getNodeByXpath('deletePath2');
                var Settrecords = SYS_getRecords(Settnode);
                for (var j = 0; j < Settrecords.length; j++) {
                    Settrecord = Settrecords[j];
                    var TSU_PO_ID = Settrecord.TSU_PO_ID;
                    if (TSU_PO_ID == namevalueObj) {
                        SYS_FilterRecordByXpath(deletePath2, namevalueObj);
                    }
                }
                // Modified by Mia--To check whether settlement record is available on the current PO Ref. If available, delete it. --End
                var RLATnode = SYS_getNodeByXpath('deletePath3');
                var RLATrecords = SYS_getRecords(RLATnode);
                for (var l = 0; l < RLATrecords.length; l++) {
                    RLATrecord = RLATrecords[l];
                    var TSU_PO_ID = RLATrecord.TSU_PO_ID;
                    if (TSU_PO_ID == namevalueObj) {
                        SYS_FilterRecordByXpath(deletePath3, namevalueObj);
                    }
                }
            }
            pRecord = SYS_getParentRecord(node);
            var TSU_COMM_REF = pRecord.TSU_COMM_REF;
            SYS_setFieldValue(node, recordId, "TSU_COMM_REF", TSU_COMM_REF);

            var sum_TSU_TTL_NET_AMT = SYS_getFieldSumValue(node, 'TSU_TTL_NET_AMT');

            var pNode = SYS_getParentDo(node);
            pRecord = SYS_getParentRecord(node);
            precorId = pRecord.recordID;
            SYS_setFieldValue(pNode, precorId, "TSU_COMM_AMT", sum_TSU_TTL_NET_AMT);
            var TSU_PO_ID = SYS_getCurrDoScreenValue("TSU_PO_ID");
            TSU_PO_ID = "%" + TSU_PO_ID + "%";
            SYS_setValueToMain("TSU_PO_ID_TEMP", TSU_PO_ID.toLowerCase());
            SYS_RefreshGrid();
        }
    } catch (e) {
        DisExcpt("SYF_TSUM_R2DataSetSubmission_DO.js", e);
    }
}

