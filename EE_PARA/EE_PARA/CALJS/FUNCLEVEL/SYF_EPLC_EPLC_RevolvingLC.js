var csFuncLevelProto = Object.create(csFuncLevelBaseProto || {});
csFuncLevelProto.ConfirmBusinessCall = function() {
    try {

        SYM_EPLC_CONFIRM_CALL();
        SYT_CHG_VOUCHER();
        SYT_LIAB_VOUCHER();
        document.MAINFORM.REV_LC_NO.value = SYS_BeInt(document.MAINFORM.REV_LC_NO.value) + 1;
    } catch (e) {
        DisExcpt("SYF_EPLC_EPLC_RevolvingLC.js", e);
    }
}

csFuncLevelProto.InitValues = function() {
    try {

        SYM_EPLC_INIT();
        document.MAINFORM.CLS_FLG.value = "NO";
        if (document.MAINFORM.REV_LC_NO.value == null || document.MAINFORM.REV_LC_NO.value == 0) {
            document.MAINFORM.REV_LC_NO.value = SYS_BeInt(document.MAINFORM.REV_LC_NO.value) + 1;
        }
        SYF_EPLC_CAL_CONF_BAL();
    } catch (e) {
        DisExcpt("SYF_EPLC_EPLC_RevolvingLC.js", e);
    }
}

csFuncLevelProto.PostconditionOnInit = function() {
    try {

        InitRun();
        SYT_CHG_INIT('SYF_EPLC_CHG_INT_TO_RUN');
        //set charge paid at as DEFERRED
        if (SYS_FUNCTION_TYPE == "PM") {
            CHG_setAllChargeAt(Chg.AT_DEFERRED);
        }
        SYM_EPLC_M_MPO_APLB_RULE_NARR(document.MAINFORM.APLB_RULE.value);
        SYM_EPLC_M_MPO_BY_TENOR_TYPE(document.MAINFORM.TENOR_TYPE.value);
        SYT_DisableDivClass("B_div");
        SYT_DisableDivClass("C_div");
        CHG_DefCharge_chargeAtOnchange();
    } catch (e) {
        DisExcpt("SYF_EPLC_EPLC_RevolvingLC.js", e);
    }
}

csFuncLevelProto.SYF_EPLC_CAL_CFM_COMM = function() {
    try {

        if (SYS_BeFloat(document.MAINFORM.CONF_BAL.value) > 0) {
            SYT_CAL_COMM('EPLC_CONF_COMM', document.MAINFORM.LC_CCY.value, document.MAINFORM.CONF_BAL.value, document.MAINFORM.ISSUE_DT.value, document.MAINFORM.EXPIRY_DT.value);
        } else {
            SYT_RESET_COMM('EPLC_CONF_COMM');
        }
    } catch (e) {
        DisExcpt("SYF_EPLC_EPLC_RevolvingLC.js", e);
    }
}

csFuncLevelProto.SYF_EPLC_CHG_INT_TO_RUN = function() {
    try {

        SYT_CAL_COMM('EPLC_OTHER_CHG', document.MAINFORM.LC_CCY.value);
        SYF_EPLC_CAL_CFM_COMM();
    } catch (e) {
        DisExcpt("SYF_EPLC_EPLC_RevolvingLC.js", e);
    }
}

csFuncLevelProto.SYF_EPLC_CAL_CONF_BAL = function() {
    try {

        var nORG_LC_BAL = SYS_BeFloat(document.MAINFORM.LC_BAL.value);
        var nORG_CONF_BAL = SYS_BeFloat(document.MAINFORM.CONF_BAL.value);
        var nREV_LC_BAL = SYS_BeFloat(document.MAINFORM.LC_AMT.value) * (1 + SYS_BeInt(document.MAINFORM.POS_TOL.value) / 100);
        var nLC_BAL = 0;
        var nCONF_BAL = 0;
        if (document.MAINFORM.CUMULATIVE.value == 'Non Cumulative') {
            nLC_BAL = nREV_LC_BAL;
            nCONF_BAL = SYS_BeFloat((nLC_BAL * SYS_BeFloat(document.MAINFORM.CONF_PCT.value)) / 100);
        } else {
            nLC_BAL = nREV_LC_BAL + nORG_LC_BAL;
            nCONF_BAL = nORG_CONF_BAL;
        }

        document.MAINFORM.LC_BAL.value = SYT_AmtFormat(document.MAINFORM.LC_CCY.value, nLC_BAL);
        document.MAINFORM.CONF_BAL.value = SYT_AmtFormat(document.MAINFORM.LC_CCY.value, nCONF_BAL);
    } catch (e) {
        DisExcpt("SYF_EPLC_EPLC_RevolvingLC.js", e);
    }
}

