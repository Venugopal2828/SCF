csFuncLevelProto.PostconditionOnInit = function() {
    try {

        InitRun();
        SYM_EPLC_M_MPO_APLB_RULE_NARR(document.MAINFORM.APLB_RULE.value);
        SYM_EPLC_M_MPO_BY_TENOR_TYPE(document.MAINFORM.TENOR_TYPE.value);
        SYT_DisableDivClass("B_div");
        SYT_DisableDivClass("C_div");
        SYT_CHG_INIT();
        if ("PM||MM||KP||EC".indexOf(SYS_FUNCTION_TYPE) > -1) {
            SYF_EPLC_CHK_ADD_CHARGE();
        }
        CHG_setAllChargeAt('1');
        CHG_allDefChargeAt_onchange();
        CHG_DefCharge_chargeAtOnchange();
    } catch (e) {
        DisExcpt("SYF_EPLC_EPLC_ExpireLC.js", e);
    }
}

csFuncLevelProto.SYF_EPLC_CHK_ADD_CHARGE = function() {
    try {

        var ccy; // Utility Auto Fix Comments
        var unPaidAmt; // Utility Auto Fix Comments
        ccy = document.MAINFORM.CHG_FLD_LOCAL_CUST_CCY.value;
        unPaidAmt = SYS_BeFloat(Chg.Screen.getLocalBalTotalAmt(ccy) + Chg.Screen.getForeignBalTotalAmt(ccy));

        if (document.MAINFORM.WRITE_OFF_CHG_FLG.value == 'NO') {
            //SYS_CheckError(document.MAINFORM.WRITE_OFF_CHG_FLG,"There are outstanding charges. Please use the Settle Charges func(EEAuto) to clear them.");
            CHG_setAllChargeAt('0');
            CHG_allDefChargeAt_onchange();
            return false;

        }

        if (document.MAINFORM.WRITE_OFF_CHG_FLG.value == 'YES') {
            CHG_setAllChargeAt('3');
            CHG_allDefChargeAt_onchange();

        }
        Chg.Screen.protectAllChargeFor();
        Chg.Screen.protectAllChargeAt();
        return true;
    } catch (e) {
        DisExcpt("SYF_EPLC_EPLC_ExpireLC.js", e);
    }
}

